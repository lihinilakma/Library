    private void txtdeladateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtdeladateMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String date = sdf.format(this.jXDatePicker1.getDate());

        SimpleDateFormat dtf = new SimpleDateFormat("dd-MM-yyyy");
        String date2 = dtf.format(this.jXDatePicker4.getDate());

        try
        {
            java.util.Date d1 = sdf. parse (date);
            java.util.Date d2 = dtf. parse (date2);

            long days = (d1.getTime()- d2.getTime())/(24*60*60*1000);
            txtdeladate.setText(""+days);

        }
        catch (ParseException pe)
        {
            JOptionPane.showMessageDialog(null, pe.getMessage());
        }
    }//GEN-LAST:event_txtdeladateMouseClicked

