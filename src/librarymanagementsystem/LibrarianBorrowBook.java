    private void txtmemidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmemidKeyPressed
        // TODO add your handling code here:

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            memid=this.txtmemid.getText();

            String Query = ("SELECT * FROM borrow_by WHERE Mem_id='"+memid+"'");

            DBCon.DBConnection();
            {
                try
                {
                    Statement st = DBCon.con.createStatement();
                    ResultSet rs = st.executeQuery(Query);
                    while(rs.next())
                    {
                        this.txtbookid.setText(rs.getString("Book_id"));
                        
                        this.txtlid.setText(rs.getString("Lib_id"));
                        
                        this.jXDatePicker3.setDate(rs.getDate("Borrow_Date"));
                        this.jXDatePicker4.setDate(rs.getDate("Due_Date"));
                        this.txtnoofbook.setText(rs.getString("No_of_Books"));
                        
                         
                         

                    }
                    DBCon.con.close();
                }
                catch(SQLException ex)
                {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }

            }
        }
    }//GEN-LAST:event_txtmemidKeyPressed

