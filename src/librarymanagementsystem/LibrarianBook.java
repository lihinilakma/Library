    private void txtbookidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbookidKeyPressed
        // TODO add your handling code here:
         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        
        bookid=this.txtbookid.getText();
        
       
         
        String Query = ("SELECT *, Quantity,Purchased_Date FROM book INNER JOIN book_quantity ON book.Book_id = book_quantity.Book_id WHERE book.Book_id = '"+bookid+"'");
         
        
        DBCon.DBConnection();
        {
        try
        {
        Statement st = DBCon.con.createStatement();
        ResultSet rs = st.executeQuery(Query);
        while(rs.next())
        {
        this.txtbookname.setText(rs.getString("Book_id"));
        this.txtprice.setText(rs.getString("Price"));
        this.txtqt.setText(rs.getString("Quantity"));
        this.txtavailable.setText(rs.getString("Available"));
        this.jXDatePicker1.setDate(rs.getDate("Purchased_Date"));
        
       
        }
        DBCon.con.close();
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        }
        }
    }//GEN-LAST:event_txtbookidKeyPressed

