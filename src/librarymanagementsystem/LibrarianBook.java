      void ClearData(){
        this.txtbookid.setText("");
        this.txtbookname.setText("");
        this.txtavailable.setText("");
        this.txtqt.setText("");
         this.jXDatePicker1.setDate(null);
        this.txtprice.setText("");
         
    }                                        

     void LoadTable()
     {
        DefaultTableModel dm = new DefaultTableModel();
      
      dm.addColumn("bookid");
      dm.addColumn("bookname");
      dm.addColumn("price");
      dm.addColumn("available");
      
      dm.addColumn("quantity");
      dm.addColumn("purchased_date");
      
       
      String Query= ("SELECT *, Quantity, Purchased_Date FROM book INNER JOIN book_quantity ON book.Book_id = book_quantity.Book_id WHERE book.Book_id = '"+bookid+"'");
      DBCon.DBConnection();
      {
          try
          {
            Statement st=DBCon.con.prepareStatement(Query);
            ResultSet rs = st. executeQuery (Query);
                                               
            while (rs.next())
            {
                String bid = rs.getString("Book_id");
                String bn= rs.getString("Book_Name");
                String price= rs.getString("Price");
                String av = rs.getString("Available");
                
                String qt = rs.getString("Quantity");
                String pd = rs.getString("Purchased_Date");
                
                 
                dm.addRow(new String[]{bid,bn,av,price,qt,pd});
            }
            this.tblbook.setModel(dm);
             
          }
           catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
          }
     
      }
    }//GEN-LAST:event_jButton5ActionPerformed

